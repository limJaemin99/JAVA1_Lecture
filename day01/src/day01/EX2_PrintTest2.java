package day01;

public class EX2_PrintTest2 {
	public static void main(String[] args) {
		System.out.println("내 이름은 \"이예진\"이에요");	// \" > "
		System.out.println("내가 좋아하는 음식은?\n 케이크");	// \n > enter역할
		//println()출력문 단축키: sysout + Ctrl + space
		
		System.out.println("코로나\t거리두기");	// \t > tab 역할
		System.out.println(3 + "단계");	// 숫자 + 문자열
		// 문자열과 다른 정수, 실수, 문자는 + 로 연결한다.
		// 문자 > '' 로 묶인것
		// 문자열 > "" 로 묶인것
		System.out.println(2 + 2);
		System.out.println(4 * 10);
		
		//35단계 라고 나온 이유?
		System.out.println(3 + ' ' + "단계");
	}

}
